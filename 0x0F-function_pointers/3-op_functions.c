#include "3-calc.h"

/**
 * op_add - Sum two numbers
 * @a: The first number
 * @b: The second number
 * Return: Results of the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts the two numbers
 * @a: The first number
 * @b: The second number
 * Return: The results of the subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies the two numbers
 * @a: The first number
 * @b: The second number
 * Return: The results of the multiplecation
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides the two numbers
 * @a: The first number
 * @b: The second number
 * Return: The results from Division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulus of two numbers
 * @a: The first number
 * @b: The second number
 * Return: The results from modulus of two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}

